func calculateEXP() []int {
	LevelGameEXP = make([]int, MaxGameLevel+1)
	for i := StartGameLevel; i < MaxGameLevel; i++ {
		mathFx := 30*((i*i)+(4*i)) - 80
		LevelGameEXP[i] = mathFx
	}
	return LevelGameEXP
}
